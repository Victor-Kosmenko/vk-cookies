    public function getJSONMessagesData() {
        $MessagesData = $this->getOption('MessagesData');
        if(is_array($MessagesData)):
            foreach ($MessagesData as $key => $value) {
                $MessagesData[$key] = stripslashes(html_entity_decode($value));
            }
        endif;
        return json_encode($MessagesData);
    }
    
